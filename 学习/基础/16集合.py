#集合 只有键key，没有value
s1={'张三','李四','花花',2,3,1,3,4}#直接创建集合
print(s1)#注意互异性
a=['张三','李四','花花',2,3,1,3,4]#利用set（）进行创建
s2=set(a)
print(s2)
b=('张三','李四','花花',2,3,1,3,4)
s3=set(b)
print(s3)
s4=set()#定义空集，={}是空字典
s4.add(80)
s4.update([200,111,22313,12312,44412])
print(s4)
#s4.remove(100)#一次移除一个值，若不存在会报错
s4.remove(111)#一次移除一个值，若不存在会报错key error
print(s4)
s4.discard(1115)#一次移除一个值，若不存在会不会报错
s4.pop()#任意删除一个元素
s4.clear()
print(s4)